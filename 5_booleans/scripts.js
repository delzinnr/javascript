// BOOLEANS
console.log('Booleans')
console.log(true)
console.log(false)
console.log(10 > 2)
console.log(5 > 200)

// COMPARAÇÕES
console.log('Comparações')
console.log(1 > 2)
console.log(5 < 10)
console.log(3 >= 3)
console.log(5 <= 4)
console.log(5 == 4)
console.log('Matheus' != 'Matheus')
console.log(3 === '3')
console.log(4 === 4)

// Operadores Lógicos
console.log('Operadores lógicos')
console.log(5 > 3 && 3 == 2)
console.log(5 > 3 || 3 == 1)
console.log(3 == 3 && 'Matheus' == 'Matheus')
console.log('Felipe' == 'João' || false)
console.log(!!(true && true))

// Operador ternário
console.log('Operador ternário')
console.log(5 > 2 ? 'mÉ si' : 'É não')
console.log(false ? 5 : 4)
console.log('Matheus' == 'Matheus' ? 'Olá, Matheus!' : 'Não é o Matheus!')
